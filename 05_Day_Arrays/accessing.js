// Acessamos cada elemento de um array usando seu índice, que começa em 0.

const fruits = ['banana', 'orange', 'mango', 'lemon'];
const firstFruit = fruits[0]; // Acessando o primeiro item usando seu índice
console.log(firstFruit);

let lastIndex = fruits.length -1; // Último elemento do array
lastFruit = fruits[lastIndex];
console.log(lastFruit); // Lemon
