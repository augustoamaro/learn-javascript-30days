// Concatenar dois arrays

const firstList = [1, 2, 3];
const secondList = [4, 5, 6];
const thirdList = firstList.concat(secondList);
console.log(thirdList); // [1, 2, 3, 4, 5, 6]

const fruits = ['banana', 'orange', 'mango', 'lemon']                 // array de frutas
const vegetables = ['Tomato', 'Potato', 'Cabbage', 'Onion', 'Carrot'] // array of vegetais
const fruitsAndVegetables = fruits.concat(vegetables)                 // concatenando os dois arrays
console.log(fruitsAndVegetables)