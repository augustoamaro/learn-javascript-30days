// O método length retorna o número de caracteres em uma string incluindo um espaço vazio.

let js = 'JavaScript';
console.log(js.length) // 10

// Podemos acessar cada caractere em uma string usando seu índice. A contagem começa em 0.
// O ultimo índice é o comprimento menos um.

let py = 'Python';
let firstLetter = py[0];
console.log(firstLetter);

let lastIndex = js.length -1 // Ultimo índice.
console.log(lastIndex);