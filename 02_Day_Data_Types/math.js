const PI = Math.PI;
console.log(PI); // 3.141592653589793

console.log(Math.round(PI)) // Arredonda o valor para o número inteiro mais próximo: 3
console.log(Math.round(9.81));

console.log(Math.floor(PI)); // Arredonda para baixo.
console.log(Math.ceil(PI)); // Arredonda para 4.

console.log(Math.min(-5, 3, 45, 550)) // Retorna o valor mínimo.
console.log(Math.max(-5, 3, 45, 550)) // Retorna o valor máximo.

const randomNumber = Math.random()
console.log(randomNumber); // Cria um número aleatório entre 0 e 0,999999.

// Criar um número aleatório entre 0 e 10.
const num = (Math.random() * 11)
console.log(num)

console.log(Math.abs(-10)) // Retorna o valor absoluto do número.
console.log(Math.sqrt(100)); // Retorna a raiz quadrada do número.

console.log(Math.pow(3, 2)) // Retorna o valor baseado no expoente. (3 na segunda).



